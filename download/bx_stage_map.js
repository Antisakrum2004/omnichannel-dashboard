// Быстрый lookup: stageId → название стадии
function bxStageName(stageId, groupId) {
  var g = BX_STAGE_MAP[String(groupId)];
  if (!g) return "";
  var s = g.stages[String(stageId)];
  return s ? s.t : "";
}

// Быстрый lookup: stageId → цвет
function bxStageColor(stageId, groupId) {
  var g = BX_STAGE_MAP[String(groupId)];
  if (!g) return "";
  var s = g.stages[String(stageId)];
  return s ? s.c : "";
}